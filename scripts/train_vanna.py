# ── Reset (wipe all training data) ──────────────────────────────────────────

def reset_training_data(vn) -> None:
    """Remove ALL training data from ChromaDB."""
    _print_header("RESET — Wiping All Training Data")

    try:
        training_df = vn.get_training_data()
    except Exception as exc:
        print(f"  ❌ Could not fetch training data: {exc}")
        return

    if training_df is None or training_df.empty:
        print("  ℹ️  No training data to delete.")
        return

    total = len(training_df)
    print(f"  Deleting {total} entries…")

    deleted = 0
    for row in training_df.itertuples(index=False):
        try:
            vn.remove_training_data(id=row.id)
            deleted += 1
        except Exception:
            pass

    print(f"  ✅ Deleted {deleted}/{total} entries.")


# ── Main ─────────────────────────────────────────────────────────────────────

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Train Vanna with DDL, documentation, and Q→SQL examples."
    )
    parser.add_argument("--ddl-only", action="store_true", help="Train DDL only")
    parser.add_argument("--docs-only", action="store_true", help="Train documentation only")
    parser.add_argument("--examples-only", action="store_true", help="Train Q→SQL examples only")
    parser.add_argument("--reset", action="store_true", help="Wipe all training data first")
    parser.add_argument("--dry-run", action="store_true", help="Preview without training")
    args = parser.parse_args()

    train_all = not (args.ddl_only or args.docs_only or args.examples_only)

    print("\n╔════════════════════════════════════════════════════════════╗")
    print("║   CHARMACY MILANO — VANNA TRAINING PIPELINE               ║")
    print("╚════════════════════════════════════════════════════════════╝")

    if args.dry_run:
        print("\n  🏃 DRY RUN MODE — no training will be performed\n")

    # ── Initialize Vanna ─────────────────────────────────────────────────
    if not args.dry_run:
        print("\n  Initialising Vanna…")
        try:
            vn = get_vanna()
            print("  ✅ Vanna ready.")
        except Exception as exc:
            print(f"  ❌ Failed to initialise Vanna: {exc}")
            print("     Run 'python scripts/test_connection.py' to diagnose.")
            sys.exit(1)
    else:
        vn = None

    # ── Reset if requested ───────────────────────────────────────────────
    if args.reset and not args.dry_run:
        reset_training_data(vn)

    # ── Training phases ──────────────────────────────────────────────────
    start_time = time.time()
    totals = {"ddl": 0, "docs": 0, "examples": 0}

    if train_all or args.ddl_only:
        totals["ddl"] = train_ddl(vn, dry_run=args.dry_run)

    if train_all or args.docs_only:
        totals["docs"] = train_documentation(vn, dry_run=args.dry_run)

    if train_all or args.examples_only:
        totals["examples"] = train_examples(vn, dry_run=args.dry_run)

    elapsed = time.time() - start_time

    # ── Summary ──────────────────────────────────────────────────────────
    _print_header("TRAINING COMPLETE")
    print(f"  DDL schemas:     {totals['ddl']}")

"""
scripts/train_vanna.py

One-time training script for the Charmacy Milano text-to-SQL chatbot.

Three training phases:
    STEP 1 — DDL:           Load .sql files from training/ddl/
    STEP 2 — Documentation: Load .md files from training/documentation/
    STEP 3 — Q→SQL Examples: Load from training/examples/seed_examples.json

Usage:
    python scripts/train_vanna.py                    # Train all 3 phases
    python scripts/train_vanna.py --ddl-only         # DDL only
    python scripts/train_vanna.py --docs-only        # Documentation only
    python scripts/train_vanna.py --examples-only    # Examples only
    python scripts/train_vanna.py --reset            # Wipe all + retrain
    python scripts/train_vanna.py --dry-run          # Preview without training

After training, verify with:
    python scripts/test_connection.py
"""


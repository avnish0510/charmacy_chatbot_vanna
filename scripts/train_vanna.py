def _print_header(title: str) -> None:
    print("\n" + "=" * 70)
    print(f"  {title}")
    print("=" * 70)


def _print_result(label: str, success: bool, detail: str = "") -> None:
    icon = "✅" if success else "❌"
    msg = f"  {icon} {label}"
    if detail:
        msg += f" — {detail}"
    print(msg)


# ── STEP 1: Train DDL ───────────────────────────────────────────────────────

def train_ddl(vn, dry_run: bool = False) -> int:
    """
    Load all .sql files from training/ddl/ and train each as DDL.

    Files should contain CREATE TABLE/VIEW statements with column
    definitions, constraints, sample data comments, and row counts
    (as generated by scripts/introspect_ssms.py).

    Returns:
        Number of DDL files successfully trained.
    """
    _print_header("STEP 1 — DDL Schema Training")

    if not DDL_DIR.exists():
        print(f"  ⚠️  DDL directory not found: {DDL_DIR}")
        print(f"      Create .sql files or run: python scripts/introspect_ssms.py --output training/ddl/")
        return 0

    sql_files = sorted(DDL_DIR.glob("*.sql"))
    if not sql_files:
        print(f"  ⚠️  No .sql files found in {DDL_DIR}")
        return 0

    print(f"  Found {len(sql_files)} DDL files:")
    trained = 0

    for sql_file in sql_files:
        filename = sql_file.name
        content = sql_file.read_text(encoding="utf-8").strip()

        if not content:
            _print_result(filename, False, "empty file — skipped")
            continue

        # Truncate display for logging
        preview = content[:120].replace("\n", " ") + "…"
        print(f"\n  📄 {filename} ({len(content):,} chars)")
        print(f"     Preview: {preview}")

        if dry_run:
            _print_result(filename, True, "DRY RUN — not trained")
            trained += 1
            continue

        try:
            vn.train(ddl=content)
            _print_result(filename, True, "trained")
            trained += 1
        except Exception as exc:
            _print_result(filename, False, str(exc)[:120])

    print(f"\n  DDL training complete: {trained}/{len(sql_files)} files")
    return trained


# ── STEP 2: Train Documentation ─────────────────────────────────────────────

def train_documentation(vn, dry_run: bool = False) -> int:
    """
    Load all .md files from training/documentation/ and train each
    as a documentation chunk.

    Also trains the T-SQL rules from core/vanna_instance.py (the same
    rules injected into every prompt) as a standalone doc chunk, so
    RAG retrieval can surface them independently of prompt injection.

    Returns:
        Number of documentation chunks successfully trained.
    """
    _print_header("STEP 2 — Documentation Training")

    trained = 0

    # ── 2a. Train T-SQL rules as documentation ───────────────────────────
    print("\n  📝 T-SQL Rules (from vanna_instance.py)")
    if dry_run:
        _print_result("T-SQL rules", True, "DRY RUN — not trained")
        trained += 1
    else:
        try:
            vn.train(documentation=TSQL_RULES)
            _print_result("T-SQL rules", True, f"{len(TSQL_RULES):,} chars")
            trained += 1
        except Exception as exc:
            _print_result("T-SQL rules", False, str(exc)[:120])

    # ── 2b. Train critical business rules inline ─────────────────────────
    critical_docs = [
        (
            "MRP definition",
            "MRP is the TOTAL line-item amount (quantity × unit price), NOT the unit price. "
            "SUM(MRP) = Total Revenue. Per-unit price = MRP / NULLIF(quantity, 0). "
            "SUM(MRP) / NULLIF(SUM(quantity), 0) = Average Selling Price (ASP). "
            "Never use AVG(MRP) for average price."
        ),
        (
            "Net sales filter",
            "Always apply the net sales filter to exclude cancelled/returned/unfulfilled rows: "
            "WHERE NOT ( "
            "(platform = 'Amazon' AND ISNULL(transaction_type,'') IN ('Cancel','Refund','FreeReplacement')) "
            "OR (platform = 'Flipkart' AND ISNULL(event_sub_type,'') IN ('Cancellation','Return','RTO')) "
            "OR (platform = 'Shopify' AND ISNULL(fulfilment_type,'') = 'unfulfilled') )"
        ),
        (
            "Order counting rules",
            "Order counting differs by platform: "
            "Amazon/Flipkart/Myntra → COUNT(DISTINCT order_id). "
            "Nykaa → SUM(total_orders). "
            "Shopify/Zepto → COUNT(*) because order_id is NULL. "
            "Cross-platform total = "
            "COUNT(DISTINCT CASE WHEN platform IN ('Amazon','Flipkart','Myntra') THEN order_id END) "
            "+ SUM(CASE WHEN platform = 'Nykaa' THEN total_orders ELSE 0 END) "
            "+ COUNT(CASE WHEN platform IN ('Shopify','Zepto') THEN 1 END)"
        ),
        (
            "Shopify has no order_date",
            "Shopify rows have NULL order_date for ALL rows. "
            "Any date filter (WHERE order_date >= ...) will silently exclude all Shopify data. "
            "Amazon also has ~87 rows with NULL order_date."
        ),
        (
            "State name normalization",
            "ship_to_state has mixed formats — abbreviation AND full name for 20 states. "
            "Always use IN() with both spellings: "
            "'Up','Uttar pradesh', 'Mh','Maharashtra', 'Dl','Delhi', 'Pb','Punjab', "
            "'Gj','Gujarat', 'Hr','Haryana', 'Wb','West bengal', 'Mp','Madhya pradesh', "
            "'Ka','Karnataka', 'Rj','Rajasthan', 'Br','Bihar', 'Ts','Tg','Telangana', "
            "'Jh','Jharkhand', 'Tn','Tamil nadu', 'Or','Odisha', 'As','Assam', "
            "'Uk','Ut','Uttarakhand', 'Kl','Kerala'"
        ),
        (
            "Product grouping rules",
            "product_name, article_type, sku_code, and EAN are NULL for ~34% of rows "
            "(products not in EAN_Master). "
            "Always add WHERE product_name IS NOT NULL when grouping by product. "
            "Use product_name for grouping — NEVER product_description (raw/unstandardised)."
        ),
        (
            "Primary vs Secondary salestype",
            "Never mix Primary and Secondary salestype in one revenue sum. "
            "Primary = Amazon, Flipkart, Myntra, Shopify, Amazon B2B (brand revenue). "
            "Secondary = Nykaa, Zepto (sell-through tracking only, not brand revenue)."
        ),
        (
            "Month year sorting",
            "month_year column values like 'Jan 2026' sort alphabetically, not chronologically. "
            "Always use ORDER BY MIN(order_date) instead of ORDER BY month_year."
        ),
    ]

    for label, doc_text in critical_docs:
        print(f"\n  📝 {label}")
        if dry_run:
            _print_result(label, True, "DRY RUN")
            trained += 1
        else:
            try:
                vn.train(documentation=doc_text)
                _print_result(label, True, f"{len(doc_text)} chars")
                trained += 1
            except Exception as exc:
                _print_result(label, False, str(exc)[:120])

    # ── 2c. Train .md files from documentation directory ─────────────────
    if DOCS_DIR.exists():
        md_files = sorted(DOCS_DIR.glob("*.md"))
        print(f"\n  Found {len(md_files)} documentation files:")

        for md_file in md_files:
            filename = md_file.name
            content = md_file.read_text(encoding="utf-8").strip()

            if not content:
                _print_result(filename, False, "empty — skipped")
                continue

            # Split large docs into chunks (ChromaDB has token limits)
            chunks = _split_doc_into_chunks(content, max_chars=3000)

            for i, chunk in enumerate(chunks):
                chunk_label = f"{filename}" if len(chunks) == 1 else f"{filename} (part {i+1}/{len(chunks)})"
                print(f"\n  📝 {chunk_label} ({len(chunk):,} chars)")

                if dry_run:
                    _print_result(chunk_label, True, "DRY RUN")
                    trained += 1
                else:
                    try:
                        vn.train(documentation=chunk)
                        _print_result(chunk_label, True, "trained")
                        trained += 1
                    except Exception as exc:
                        _print_result(chunk_label, False, str(exc)[:120])
    else:
        print(f"  ⚠️  Documentation directory not found: {DOCS_DIR}")

    print(f"\n  Documentation training complete: {trained} chunks")
    return trained


def _split_doc_into_chunks(text: str, max_chars: int = 3000) -> list[str]:
    """Split a long document into chunks at paragraph boundaries."""
    if len(text) <= max_chars:
        return [text]

    chunks = []
    paragraphs = text.split("\n\n")
    current_chunk = ""

    for para in paragraphs:
        if len(current_chunk) + len(para) + 2 > max_chars and current_chunk:
            chunks.append(current_chunk.strip())
            current_chunk = para
        else:
            current_chunk += "\n\n" + para if current_chunk else para

    if current_chunk.strip():
        chunks.append(current_chunk.strip())

    return chunks if chunks else [text]


# ── STEP 3: Train Q→SQL Examples ────────────────────────────────────────────

def train_examples(vn, dry_run: bool = False) -> int:
    """
    Load Q→SQL examples from training/examples/seed_examples.json
    and train each pair.

    Expected JSON format:
    [
        {"question": "What is total revenue?", "sql": "SELECT SUM(MRP)..."},
        ...
    ]

    Returns:
        Number of examples successfully trained.
    """
    _print_header("STEP 3 — Q→SQL Example Training")

    if not EXAMPLES_FILE.exists():
        print(f"  ⚠️  Examples file not found: {EXAMPLES_FILE}")
        print(f"      Create it with Q→SQL pairs for best accuracy.")

        # Create the directory and a starter file
        EXAMPLES_FILE.parent.mkdir(parents=True, exist_ok=True)
        starter = _get_starter_examples()

        with open(EXAMPLES_FILE, "w", encoding="utf-8") as fh:
            json.dump(starter, fh, indent=2, ensure_ascii=False)
        print(f"      ✅ Created starter file with {len(starter)} examples.")
        print(f"      Edit it and re-run this script.")

        # Train the starter examples
        examples = starter
    else:
        try:
            with open(EXAMPLES_FILE, "r", encoding="utf-8") as fh:
                examples = json.load(fh)
        except json.JSONDecodeError as exc:
            print(f"  ❌ Invalid JSON in {EXAMPLES_FILE}: {exc}")
            return 0

    if not isinstance(examples, list):
        print(f"  ❌ Expected a JSON array, got {type(examples).__name__}")
        return 0

    print(f"  Found {len(examples)} Q→SQL examples")
    trained = 0
    failed = 0

    for i, item in enumerate(examples, 1):
        question = item.get("question", "").strip()
        sql = item.get("sql", "").strip()

        if not question or not sql:
            _print_result(f"Example {i}", False, "missing question or sql")
            failed += 1
            continue

        q_preview = question[:70] + "…" if len(question) > 70 else question
        print(f"\n  [{i}/{len(examples)}] Q: {q_preview}")

        if dry_run:
            _print_result(f"Example {i}", True, "DRY RUN")
            trained += 1
            continue

        try:
            vn.train(question=question, sql=sql)
            _print_result(f"Example {i}", True, "trained")
            trained += 1
        except Exception as exc:
            _print_result(f"Example {i}", False, str(exc)[:120])
            failed += 1

    print(f"\n  Example training complete: {trained}/{len(examples)} "
          f"(failed: {failed})")

    if trained < 20:
        print(f"\n  ⚠️  WARNING: Only {trained} examples trained.")
        print(f"      For reliable SQL generation, aim for 50+ diverse examples.")

    return trained


